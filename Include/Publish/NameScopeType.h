#pragma once
namespace Aspose {
namespace Cells {
			/// <summary>
			/// Represents the scope type of defined names.
			/// </summary>
	enum NameScopeType 

	{

			/// <summary>
			/// All defined names.
			/// </summary>
			NameScopeType_All ,
			/// <summary>
			/// The defined names in the workbook.
			/// </summary>
			NameScopeType_Workbook ,
			/// <summary>
			/// The defined names in a worksheet or all worksheets.
			/// </summary>
			NameScopeType_Worksheet ,
	};


}

}
