#pragma once
namespace Aspose {
namespace Cells {
namespace Rendering {
			/// <summary>
			/// Enumerates Bit Depth Type for tiff image.
			/// </summary>
	enum ColorDepth 

	{

			/// <summary>
			/// Default value, not set value.
			/// </summary>
			ColorDepth_Default = 0 ,
			/// <summary>
			/// 1 bit per pixel
			/// </summary>
			ColorDepth_Format1bpp = 1 ,
			/// <summary>
			/// 4 bits per pixel
			/// </summary>
			ColorDepth_Format4bpp = 4 ,
			/// <summary>
			/// 8 bits per pixel
			/// </summary>
			ColorDepth_Format8bpp = 8 ,
			/// <summary>
			/// 24 bits per pixel
			/// </summary>
			ColorDepth_Format24bpp = 24 ,
			/// <summary>
			/// 32 bits per pixel
			/// </summary>
			ColorDepth_Format32bpp = 32 ,
	};


}

}

}
