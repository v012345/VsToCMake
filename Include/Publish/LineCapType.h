#pragma once
namespace Aspose {
namespace Cells {
namespace Drawing {
			/// <summary>
			/// Represents the caps of a line
			/// </summary>
	enum LineCapType 

	{

			/// <summary>
			/// Square protrudes by half line width.
			/// </summary>
			LineCapType_Square ,
			/// <summary>
			/// Rounded ends.
			/// </summary>
			LineCapType_Round ,
			/// <summary>
			/// Line ends at end point.
			/// </summary>
			LineCapType_Flat ,
			/// <summary>
			/// None cap
			/// </summary>
			LineCapType_None ,
	};


}

}

}
