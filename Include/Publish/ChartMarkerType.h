#pragma once
namespace Aspose {
namespace Cells {
namespace Charts {
			/// <summary>
			/// Represents the marker style in a line chart, scatter chart, or radar chart. 
			/// </summary>
	enum ChartMarkerType 

	{

			/// <summary>
			/// Automatic markers.
			/// </summary>
			ChartMarkerType_Automatic ,
			/// <summary>
			/// Circular markers.
			/// </summary>
			ChartMarkerType_Circle ,
			/// <summary>
			/// Long bar markers
			/// </summary>
			ChartMarkerType_Dash ,
			/// <summary>
			/// Diamond-shaped markers.
			/// </summary>
			ChartMarkerType_Diamond ,
			/// <summary>
			/// Short bar markers.
			/// </summary>
			ChartMarkerType_Dot ,
			/// <summary>
			/// No markers.
			/// </summary>
			ChartMarkerType_None ,
			/// <summary>
			/// Square markers with a plus sign.
			/// </summary>
			ChartMarkerType_SquarePlus ,
			/// <summary>
			/// Square markers.
			/// </summary>
			ChartMarkerType_Square ,
			/// <summary>
			/// Square markers with an asterisk.
			/// </summary>
			ChartMarkerType_SquareStar ,
			/// <summary>
			/// Triangular markers.
			/// </summary>
			ChartMarkerType_Triangle ,
			/// <summary>
			/// Square markers with an X.
			/// </summary>
			ChartMarkerType_SquareX ,
			/// <summary>
			/// Picture
			/// </summary>
			ChartMarkerType_Picture ,
	};


}

}

}
