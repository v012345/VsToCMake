#pragma once
namespace Aspose {
namespace Cells {
namespace Drawing {
			/// <summary>
			/// Fill format type.
			/// </summary>
	enum FillType 

	{

			/// <summary>
			/// Represents automatic formatting type.
			/// </summary>
			FillType_Automatic ,
			/// <summary>
			/// Represents none formatting type.
			/// </summary>
			FillType_None ,
			/// <summary>
			/// Solid fill format.
			/// </summary>
			FillType_Solid ,
			/// <summary>
			/// Gradient fill format.
			/// </summary>
			FillType_Gradient ,
			/// <summary>
			/// Texture fill format(inculdes picuture fill).
			/// </summary>
			FillType_Texture ,
			/// <summary>
			/// Pattern fill format.
			/// </summary>
			FillType_Pattern ,
			/// <summary>
			/// Inherit the fill properties of the group.
			/// </summary>
			FillType_Group ,
	};


}

}

}
