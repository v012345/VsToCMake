#pragma once
namespace Aspose {
namespace Cells {
namespace Drawing {
			/// <summary>
			/// Specifies the type (format) of an image.
			/// </summary>
	enum ImageType 

	{

			/// <summary>
			/// An unknown image type.
			/// </summary>
			ImageType_Unknown = 0 ,
			/// <summary>
			/// Windows Enhanced Metafile.
			/// </summary>
			ImageType_Emf = 2 ,
			/// <summary>
			/// Windows Metafile. 
			/// </summary>
			ImageType_Wmf = 3 ,
			/// <summary>
			/// Macintosh PICT.
			/// </summary>
			ImageType_Pict = 4 ,
			/// <summary>
			/// JPEG JFIF.
			/// </summary>
			ImageType_Jpeg = 5 ,
			/// <summary>
			/// Portable Network Graphics.
			/// </summary>
			ImageType_Png = 6 ,
			/// <summary>
			/// Windows Bitmap
			/// </summary>
			ImageType_Bmp = 7 ,
			/// <summary>
			/// Gif
			/// </summary>
			ImageType_Gif = 66 ,
			/// <summary>
			/// Tiff
			/// </summary>
			ImageType_Tiff = 67 ,
			/// <summary>
			/// Svg
			/// </summary>
			ImageType_Svg = 68 ,
			/// <summary>
			/// Svm
			/// </summary>
			ImageType_Svm = 69 ,
			/// <summary>
			/// glTF
			/// </summary>
			ImageType_Gltf = 70 ,
			/// <summary>
			/// Windows Enhanced Metafile which is more compatible with Office.
			/// </summary>
			ImageType_OfficeCompatibleEmf = 71 ,
			/// <summary>
			/// Weppy image format
			/// </summary>
			ImageType_WebP = 72 ,
	};


}

}

}
