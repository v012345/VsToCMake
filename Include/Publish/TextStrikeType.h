namespace Cells {
			/// <summary>
			/// This type specifies the strike type.
			/// </summary>
	enum TextStrikeType 
	{

			/// <summary>
			/// A single strikethrough applied on the text
			/// </summary>
			TextStrikeType_Single ,
			/// <summary>
			/// A double strikethrough applied on the text
			/// </summary>
			TextStrikeType_Double ,
			/// <summary>
			/// No strike is applied to the text
			/// </summary>
			TextStrikeType_None ,
	};


}

