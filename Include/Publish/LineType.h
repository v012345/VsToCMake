#pragma once
namespace Aspose {
namespace Cells {
namespace Drawing {
			/// <summary>
			/// Enumerates the type of <see cref="Picture" />
			///  border or <see cref="Chart" />
			///  line.
			/// </summary>
	enum LineType 

	{

			/// <summary>
			/// Represents a dark gray line.
			/// </summary>
			LineType_DarkGray = 6 ,
			/// <summary>
			/// Represent a dash line.
			/// </summary>
			LineType_Dash = 1 ,
			/// <summary>
			/// Represents a dash-dot line
			/// </summary>
			LineType_DashDot = 3 ,
			/// <summary>
			/// Represents a dash-dot-dot line.
			/// </summary>
			LineType_DashDotDot = 4 ,
			/// <summary>
			/// Represents a dotted line.
			/// </summary>
			LineType_Dot = 2 ,
			/// <summary>
			/// Represents a light gray line.
			/// </summary>
			LineType_LightGray = 8 ,
			/// <summary>
			/// Represents a medium gray line.
			/// </summary>
			LineType_MediumGray = 7 ,
			/// <summary>
			/// Represent a solid line.
			/// </summary>
			LineType_Solid = 0 ,
	};


}

}

}
