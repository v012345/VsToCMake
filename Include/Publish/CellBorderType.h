#pragma once
namespace Aspose {
namespace Cells {
			/// <summary>
			/// Enumerates a cell's border type. 
			/// </summary>
	enum CellBorderType 

	{

			/// <summary>
			/// Represents thin dash-dotted line.
			/// </summary>
			CellBorderType_DashDot = 9 ,
			/// <summary>
			/// Represents thin dash-dot-dotted line.
			/// </summary>
			CellBorderType_DashDotDot = 11 ,
			/// <summary>
			/// Represents dashed line.
			/// </summary>
			CellBorderType_Dashed = 3 ,
			/// <summary>
			/// Represents dotted line.
			/// </summary>
			CellBorderType_Dotted = 4 ,
			/// <summary>
			/// Represents double line.
			/// </summary>
			CellBorderType_Double = 6 ,
			/// <summary>
			/// Represents hair line.
			/// </summary>
			CellBorderType_Hair = 7 ,
			/// <summary>
			/// Represents medium dash-dotted line.
			/// </summary>
			CellBorderType_MediumDashDot = 10 ,
			/// <summary>
			/// Represents medium dash-dot-dotted line.
			/// </summary>
			CellBorderType_MediumDashDotDot = 12 ,
			/// <summary>
			/// Represents medium dashed line.
			/// </summary>
			CellBorderType_MediumDashed = 8 ,
			/// <summary>
			/// Represents no line.
			/// </summary>
			CellBorderType_None = 0 ,
			/// <summary>
			/// Represents medium line.
			/// </summary>
			CellBorderType_Medium = 2 ,
			/// <summary>
			/// Represents slanted medium dash-dotted line.
			/// </summary>
			CellBorderType_SlantedDashDot = 13 ,
			/// <summary>
			/// Represents thick line.
			/// </summary>
			CellBorderType_Thick = 5 ,
			/// <summary>
			/// Represents thin line.
			/// </summary>
			CellBorderType_Thin = 1 ,
	};


}

}
