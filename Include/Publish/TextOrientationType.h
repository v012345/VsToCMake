#pragma once
namespace Aspose {
namespace Cells {
			/// <summary>
			/// Enumerates text orientation types.
			/// </summary>
	enum TextOrientationType 

	{

			/// <summary>
			/// text is rotated 90 degrees clockwise
			/// </summary>
			TextOrientationType_ClockWise ,
			/// <summary>
			/// text is rotated 90 degrees counterclockwise
			/// </summary>
			TextOrientationType_CounterClockWise ,
			/// <summary>
			/// Represents the default value.
			/// </summary>
			TextOrientationType_NoRotation ,
			/// <summary>
			/// Displays text from top to bottom of the cell. Stacked text.
			/// </summary>
			TextOrientationType_TopToBottom ,
	};


}

}
