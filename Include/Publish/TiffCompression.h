#pragma once
namespace Aspose {
namespace Cells {
namespace Rendering {
			/// <summary>
			/// Specifies what type of compression to apply when saving images into TIFF format file. 
			/// </summary>
	enum TiffCompression 

	{

			/// <summary>
			/// Specifies no compression. 
			/// </summary>
			TiffCompression_CompressionNone = 0 ,
			/// <summary>
			/// Specifies the RLE compression scheme. 
			/// </summary>
			TiffCompression_CompressionRle = 1 ,
			/// <summary>
			/// Specifies the LZW compression scheme. 
			/// </summary>
			TiffCompression_CompressionLZW = 2 ,
			/// <summary>
			/// Specifies the CCITT3 compression scheme. 
			/// </summary>
			TiffCompression_CompressionCCITT3 = 3 ,
			/// <summary>
			/// Specifies the CCITT4 compression scheme. 
			/// </summary>
			TiffCompression_CompressionCCITT4 = 4 ,
	};


}

}

}
