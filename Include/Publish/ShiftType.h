#pragma once
namespace Aspose {
namespace Cells {
			/// <summary>
			/// Represent the shift options when deleting a range of cells.
			/// </summary>
			/// 
			/// 
	enum ShiftType 
	{

			/// <summary>
			/// Shift cells down.
			/// </summary>
			/// 
			/// 
			ShiftType_Down ,
			/// <summary>
			/// Shift cells left.
			/// </summary>
			/// 
			/// 
			ShiftType_Left ,
			/// <summary>
			/// Do not shift cells.
			/// </summary>
			/// 
			/// 
			ShiftType_None ,
			/// <summary>
			/// Shift cells right.
			/// </summary>
			/// 
			/// 
			ShiftType_Right ,
			/// <summary>
			/// Shift cells up.
			/// </summary>
			/// 
			/// 
			ShiftType_Up ,
	};


}

}
