#pragma once
namespace Aspose {
namespace Cells {
			/// <summary>
			/// Represents the type of target mode.
			/// </summary>
	enum TargetModeType 

	{

			/// <summary>
			/// External link
			/// </summary>
			TargetModeType_External ,
			/// <summary>
			/// Local and full paths to files or folders.
			/// </summary>
			TargetModeType_FilePath ,
			/// <summary>
			/// Email.
			/// </summary>
			TargetModeType_Email ,
			/// <summary>
			/// Link on cell or named range.
			/// </summary>
			TargetModeType_CellReference ,
	};


}

}
