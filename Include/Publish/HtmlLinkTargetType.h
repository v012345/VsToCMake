#pragma once
namespace Aspose {
namespace Cells {
			/// <summary>
			/// Represents the type of target attribute in HTML <a/>
			///  tag.
			/// </summary>
	enum HtmlLinkTargetType 

	{

			/// <summary>
			/// Opens the linked document in a new window or tab
			/// </summary>
			HtmlLinkTargetType_Blank ,
			/// <summary>
			/// Opens the linked document in the parent frame
			/// </summary>
			HtmlLinkTargetType_Parent ,
			/// <summary>
			/// Opens the linked document in the same frame as it was clicked (this is default)
			/// </summary>
			HtmlLinkTargetType_Self ,
			/// <summary>
			/// Opens the linked document in the full body of the window
			/// </summary>
			HtmlLinkTargetType_Top ,
	};


}

}
