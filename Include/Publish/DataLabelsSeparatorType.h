#pragma once
namespace Aspose {
namespace Cells {
namespace Charts {
			/// <summary>
			/// Represents the separator type of DataLabels.  
			/// </summary>
	enum DataLabelsSeparatorType 

	{

			/// <summary>
			/// Represents automatic separator
			/// </summary>
			DataLabelsSeparatorType_Auto = 0 ,
			/// <summary>
			/// Represents space(" ")
			/// </summary>
			DataLabelsSeparatorType_Space = 1 ,
			/// <summary>
			/// Represents comma(",")
			/// </summary>
			DataLabelsSeparatorType_Comma = 2 ,
			/// <summary>
			/// Represents semicolon(";")
			/// </summary>
			DataLabelsSeparatorType_Semicolon = 3 ,
			/// <summary>
			/// Represents period(".")
			/// </summary>
			DataLabelsSeparatorType_Period = 4 ,
			/// <summary>
			/// Represents newline("\n")
			/// </summary>
			DataLabelsSeparatorType_NewLine = 5 ,
			/// <summary>
			/// Represents custom separator
			/// </summary>
			DataLabelsSeparatorType_Custom = 6 ,
	};


}

}

}
