#pragma once
namespace Aspose {
namespace Cells {
			/// <summary>
			/// Enumerates text alignment types. 
			/// </summary>
	enum TextAlignmentType 
	{

			/// <summary>
			/// Represents general text alignment.
			/// </summary>
			TextAlignmentType_General = 0x0700 ,
			/// <summary>
			/// Represents bottom text alignment.
			/// </summary>
			TextAlignmentType_Bottom = 0x0210 ,
			/// <summary>
			/// Represents center text alignment.
			/// </summary>
			TextAlignmentType_Center = 0x070A ,
			/// <summary>
			/// Represents center across text alignment.
			/// </summary>
			TextAlignmentType_CenterAcross = 0x0506 ,
			/// <summary>
			/// Represents distributed text alignment.
			/// </summary>
			TextAlignmentType_Distributed = 0x0327 ,
			/// <summary>
			/// Represents fill text alignment.
			/// </summary>
			TextAlignmentType_Fill = 0x0504 ,
			/// <summary>
			/// Represents justify text alignment.
			/// </summary>
			TextAlignmentType_Justify = 0x071D ,
			/// <summary>
			/// Represents left text alignment.
			/// </summary>
			TextAlignmentType_Left = 0x0101 ,
			/// <summary>
			/// Represents right text alignment.
			/// </summary>
			TextAlignmentType_Right = 0x0103 ,
			/// <summary>
			/// Represents top text alignment.
			/// </summary>
			TextAlignmentType_Top = 0x0200 ,
			/// <summary>
			/// Aligns the text with an adjusted kashida length for Arabic text.
			/// </summary>
			TextAlignmentType_JustifiedLow = 0x0340 ,
			/// <summary>
			/// Distributes Thai text specially, because each character is treated as a word.
			/// </summary>
			TextAlignmentType_ThaiDistributed = 0x0380 ,
	};


}

}
