#pragma once
namespace Aspose {
namespace Cells {
			/// <summary>
			/// Custom Filter operator type.
			/// </summary>
	enum FilterOperatorType 

	{

			/// <summary>
			/// Represents LessOrEqual operator.
			/// </summary>
			FilterOperatorType_LessOrEqual ,
			/// <summary>
			/// Represents LessThan operator.
			/// </summary>
			FilterOperatorType_LessThan ,
			/// <summary>
			/// Represents Equal operator.
			/// </summary>
			FilterOperatorType_Equal ,
			/// <summary>
			/// Represents GreaterThan operator.
			/// </summary>
			FilterOperatorType_GreaterThan ,
			/// <summary>
			/// Represents NotEqual operator.
			/// </summary>
			FilterOperatorType_NotEqual ,
			/// <summary>
			/// Represents GreaterOrEqual operator.
			/// </summary>
			FilterOperatorType_GreaterOrEqual ,
			/// <summary>
			/// Represents no comparison.
			/// </summary>
			FilterOperatorType_None ,
			/// <summary>
			/// Begins with the text.
			/// </summary>
			FilterOperatorType_BeginsWith ,
			/// <summary>
			/// Ends with the text.
			/// </summary>
			FilterOperatorType_EndsWith ,
			/// <summary>
			/// Contains the text.
			/// </summary>
			FilterOperatorType_Contains ,
			/// <summary>
			/// Not contains the text.
			/// </summary>
			FilterOperatorType_NotContains ,
	};


}

}
