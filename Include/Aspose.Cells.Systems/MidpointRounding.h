#ifndef _SYSTEM_MIDPOINTROUNDING_H_
#define _SYSTEM_MIDPOINTROUNDING_H_

namespace Aspose { 
	namespace Cells { 
		namespace Systems {
			enum MidpointRounding {
				// Summary:
				//     When a number is halfway between two others, it is rounded toward the nearest
				//     even number->
				MidpointRounding_ToEven = 0,
				//
				// Summary:
				//     When a number is halfway between two others, it is rounded toward the nearest
				//     number that is away from zero->
				MidpointRounding_AwayFromZero = 1,
			};
		}
	}
}
#endif//_SYSTEM_MIDPOINTROUNDING_H_